namespace mvdmio.Hotwire.NET.ASP.Enums;

/// <summary>
/// Replacement methods supported by Turbo.
/// </summary>
public enum TurboReplaceMethod
{
   /// <summary>
   /// Default replacement method. Replaces the HTML content with the content in the template.
   /// </summary>
   None,
   
   /// <summary>
   /// Uses morphing to replace the HTML content with the content in the template.
   /// </summary>
   Morph
}